package Odev;

public class Courses {
	
	int id;
	String courseName;
	String content;
	String price;
	
	public Courses() {
		
	}
	
	public Courses(int id, String courseName, String content,String price) {
		this.id = id;
		this.courseName = courseName;
		this.content = content;
		this.price = price;
	}
	

}
